                            </a>

                            <!-- Answer 03 -->
                            <p id="r3" class="answer">
                                SIP+MGO consiste em um produto composto por duas placas de MGO (óxido de magnésio e cloreto de magnésio) preenchidas por SIP sigla em inglês: (Structural Insulated Panels) ou  (isolante térmico e acústico). As placas são leves, de fácil manuseio e instalação, possuem alta resistência mecânica e contra umidade, além de ser um material naturalmente à prova de fogo. <br>
                                As placas de MGO são Eco-friendly, fabricadas a partir de minerais naturais, compostas por fibras vegetais e não contém substâncias nocivas em sua composição. Possuem altíssima resistência física e térmica, podendo suportar até 2000°C de calor sem produzir nenhuma fumaça tóxica.
                            </p>

